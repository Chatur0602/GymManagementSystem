    private void paymentComboBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_paymentComboBoxItemStateChanged
        int amount = 0 ;
        String feedback = null ;
        
        String payment = paymentComboBox.getSelectedItem().toString();
        char paymentID = payment.charAt(0);
        int pID = Character.getNumericValue(paymentID);
        
        for (Payment list : allPayments) {
            if(list.getID() == pID){
               amount = list.getAmount();
               feedback = list.getFeedback();
            }
        }
       
        amountField.setText(String.valueOf(amount));
        feedbackTextBox.setText(feedback);
    }//GEN-LAST:event_paymentComboBoxItemStateChanged

    
